#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 == NULL || s2 == NULL)
		return (0);
	if (n == 0)
		return (1);
	n -= 1;
	while (*s1 && *s2 && n-- > 0)
	{
		if (*s1++ != *s2++)
			return (0);
	}
	return (*s1 == *s2 ? 1 : 0);
}
