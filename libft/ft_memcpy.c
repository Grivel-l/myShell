#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*result;
	char	*source;

	result = dst;
	source = (void *)src;
	while (n-- > 0)
		*result++ = *source++;
	return (dst);
}
