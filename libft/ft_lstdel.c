#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*list;
	t_list	*pointer;

	list = *alst;
	*alst = NULL;
	pointer = NULL;
	while (list != NULL)
	{
		pointer = list->next;
		ft_lstdelone(&list, del);
		list = pointer;
	}
}
