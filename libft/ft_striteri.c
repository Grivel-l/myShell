#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	i = 0;
	while (s != NULL && f != NULL && s[i])
	{
		f(i, &s[i]);
		i += 1;
	}
}
